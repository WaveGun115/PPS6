package planta;

/**
 *
 * @author Kevin Benitez
 */
public class Propiedades
{

    private int nivelagua = 0;
    private int nivelabono = 0;
    private int crecimiento = 0;

    /**
     * @return the nivelagua
     */
    public int getNivelagua()
    {
        return nivelagua;
    }

    /**
     * @param nivelagua the nivelagua to set
     */
    public void setNivelagua(int nivelagua)
    {
        this.nivelagua = nivelagua;
    }

    /**
     * @return the nivelabono
     */
    public int getNivelabono()
    {
        return nivelabono;
    }

    /**
     * @param nivelabono the nivelabono to set
     */
    public void setNivelabono(int nivelabono)
    {
        this.nivelabono = nivelabono;
    }

    public int getCrecimiento()
    {
        return crecimiento;
    }

    public void setCrecimiento(int crecimiento)
    {
        this.crecimiento = crecimiento;
    }

}
